#area and perimeter of circle and rectangle
#CIRCLE
r = 7
pi = 3.14
circle_area = pi*(r**2)
#Perimeter of Circle
circle_perimeter = 2*pi*r
print(f"Circle (r={r}):")
print(f"  Area: {circle_area}")
print(f"  Perimeter: {circle_perimeter}")

#RECTANGLE
length = 3
width = 3
rectangle_area = length*width
#Perimeter of Rectangle
rectangle_perimeter = 2*(length + width)
print(f"\nRectangle ({length} x {width}):")
print(f"  Area: {rectangle_area}")
print(f"  Perimeter: {rectangle_perimeter}")